fun main(){
    // int
    val test1 = 1
    val test2 = 1
    println(test1 === test2)
    // double
    val test3 = 1.0
    val test4 = 1.0
    println(test3 === test4)
    // float
    val test5 = 1.0f
    val test6 = 1.0f
    println(test5 === test6)
    // string
    val test7 = "1.2f"
    val test8 = "1.0f"
    println(test7 === test8)
    // boolean
    val test9 = true
    val test10 = true
    println(test9 === test10)
    // char
    val test11 = 'c'
    val test12 = 'c'
    println(test11 === test12)
}